//Criando Variaveis em Java

public class variaveis{
    public static void main(String[] args){
        int farinha = 2;

        System.out.println("A receita requer: " + farinha + " xicaras de farinha");
    }
}

/* Oque são Variaveis
 * Variaveis são espaços de memória que reservamos para armazenar valores
 * Variaveis podem ser do mesmo tipo ou de tipos diferentes
 * Variaveis podem ser declaradas com ou sem valores
 * Variaveis podem ser inicializadas com valores ou não
 */
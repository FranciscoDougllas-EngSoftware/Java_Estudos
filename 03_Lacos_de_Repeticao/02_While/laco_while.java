//Laco de repeticao while
//O laco de repeticao while e um laco de repeticao que executa 
//um bloco de codigo enquanto a condicao for verdadeira
public class laco_while {
    public static void main(String[] args){
        int i = 0;
        while (i < 10){
            System.out.println("i = " + i);
            i++;
        }
    }
}
